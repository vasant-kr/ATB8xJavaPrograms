package ex_20092024;

public class Lab066_TernaryOperator_Practical2 {
    public static void main(String[] args) {
        // Max between two numbers by using ternary operator

        int a = 10;
        int b = 20;
        int max = a > b ? a : b;
        int min = a < b ? a : b;
        System.out.println(max);
        System.out.println(min);
    }
}
