package ex_20092024;

public class Lab065_TernaryOperator_Practical {
    public static void main(String[] args) {
        // Max between two numbers by using ternary operator

        int tushar_salary = 4;
        int prabhu_salary = 7;
        // max
        // min
        int max = tushar_salary > prabhu_salary ? tushar_salary : prabhu_salary;
        System.out.println(max);
    }
}
