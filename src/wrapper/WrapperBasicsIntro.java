    /*
    Wrapper classes in Java are essential components that allow primitive datatypes to be treated as objects
    For some extra functions over primitive data types
    There are no functions/methods in data types. This is why Java is not 100% OOPS
    By adding a wrapper then we have a lot of methods available

    Basically for boxing and Unboxing:

    Primitive datatypes to Wrapper Classes - Boxing
    Wrapper classes to primitive datatypes - Unboxing

    Autoboxing : Integer a = 5;
    Unboxing: int i = a;


In Collection framework - We can only use Wrapper classes as we can only use classes and objects

     */
