package ex_25092024;

import java.util.Scanner;

public class Lab_094_WhileLoop_Practical1 {
    public static void main(String[] args) {

        //while loop example
        //condition is checked first and then the execution of the statements or code
        // So in the case of for statement and while statement, when first time condition is false
        //then the block will not be executed.

        //- or and while statement are also called as **Entry Controlled Loop.**

        int i = 0;
        while (i<10){
            System.out.println(i);
            i++;
        }

    }
}

