package ex_25092024;

public class Lab_089_PrintEvenNumbers {
    public static void main(String[] args) {

        // print even numbers between 0 to 50

        for (int i = 0; i <= 50; i++) { // 0 to 9
            if (i % 2 == 0) {
                System.out.println(i);
            }

        }
    }
}
