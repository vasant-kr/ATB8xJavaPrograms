package ex_25092024;

public class Lab_087_ForLoop_Practical3 {
    public static void main(String[] args) {

        /*
        we can initiate outside the loop as well
         */
        int i =10;

        for (; i >0; i--){
            System.out.println(i);
        }

    }
}

